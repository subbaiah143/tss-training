public class InheritenceGrand {

	protected int num1 = 10;
	protected int num2 = 20;

	public void add() {
		System.out.println("adding of two numbers " + " " + (num1 + num2));
	}

	public void sub() {
		System.out.println("subtracting of two numbers" + " " + (num1 - num2));
	}
}
